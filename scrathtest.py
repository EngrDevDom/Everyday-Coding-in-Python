fruits = [ 'apple', 'banana', 'grapes',
           'kenu', 'apple', 'banana', 'apple' ]

fruits.sort()
print("Sorted fruits    >> ", fruits)

cp = fruits.copy()
print("Copied fruits    >> ", cp)

fruits.reverse()
print("Reversed fruits  >> ", fruits)

fruits.clear()
print("Cleared fruits   >> ", fruits)
